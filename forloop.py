#implementing the for loop using range function
for i in range(5):
    print(i)

#changing the starting point inn the range function
for i in range(1,5):
    print(i)

#changing the increment value in the loop using the range function
for i in range(1,30,2):
    print(i)
#printing the values in a descending order
for i in range(30,1,-1):
    print(i)